#Pseudocode
#1. Prompt user to enter input, number which will be the radius
#2. prompt user to enter input, number which will be the lenght 
#3. Look for the area of the cylinder
#4. To get volume, multiply area X radius X radius X lenght
#5. print result

radius = int(input("Enter radius: "))
length = int(input("Enter length: "))
PI = 3.14
volume = (PI ** radius * length)
print(volume)




 